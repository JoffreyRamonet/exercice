package model;


public class Point {

	private final Integer ordonate;
	private final Integer abscissa;
	
	public Point(Integer ordonate, Integer abscissa) {
		this.ordonate = ordonate;
		this.abscissa = abscissa;
	}
	
	public Integer getOrdonate() {
		return ordonate;
	}

	public Integer getAbscissa() {
		return abscissa;
	}

	
	
}
